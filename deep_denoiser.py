import torch, os
import numpy as np
import torch.nn as nn
import torch.nn.init as init

class DnCNN(nn.Module):

    def __init__(self, depth=17, n_channels=64, image_channels=1, use_bnorm=True, kernel_size=3):
        super(DnCNN, self).__init__()
        kernel_size = 3
        padding = 1
        layers = []
        layers.append(nn.Conv2d(in_channels=image_channels, out_channels=n_channels, kernel_size=kernel_size, padding=padding, bias=True))
        layers.append(nn.ReLU(inplace=True))
        for _ in range(depth-2):
            layers.append(nn.Conv2d(in_channels=n_channels, out_channels=n_channels, kernel_size=kernel_size, padding=padding, bias=False))
            layers.append(nn.BatchNorm2d(n_channels, eps=0.0001, momentum=0.95))
            layers.append(nn.ReLU(inplace=True))
        layers.append(nn.Conv2d(in_channels=n_channels, out_channels=image_channels, kernel_size=kernel_size, padding=padding, bias=False))
        self.dncnn = nn.Sequential(*layers)
        self._initialize_weights()

    def forward(self, x):
        y = x
        out = self.dncnn(x)
        return y-out

    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                init.orthogonal_(m.weight)
                print('init weight')
                if m.bias is not None:
                    init.constant_(m.bias, 0)
            elif isinstance(m, nn.BatchNorm2d):
                init.constant_(m.weight, 1)
                init.constant_(m.bias, 0)

def denoise_with_network(input_image):
    cwd = os.getcwd()
    model_path = cwd + '''/dncnn_pytorch/models/DnCNN_sigma25/model.pth'''
    model = torch.load(model_path, map_location=torch.device('cpu'))

    model.eval()  # evaluation mode

    y = input_image
    y = y.astype(np.float32)
    y_ = torch.from_numpy(y).view(1, -1, y.shape[0], y.shape[1])

    reconstruction = model(y_)  # inference
    reconstruction = reconstruction.view(y.shape[0], y.shape[1])
    return reconstruction.detach().numpy().astype(np.float32)






